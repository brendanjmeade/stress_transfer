function [C] = ReadCmt(filename)
dataDump      = textread(filename, '%s');
C.lon         = str2num(char(dataDump(1:11:end)));
C.lat         = str2num(char(dataDump(2:11:end)));
C.strike1     = str2num(char(dataDump(3:11:end)));
C.dip1        = str2num(char(dataDump(4:11:end)));
C.rake1       = str2num(char(dataDump(5:11:end)));
C.strike2     = str2num(char(dataDump(6:11:end)));
C.dip2        = str2num(char(dataDump(7:11:end)));
C.rake2       = str2num(char(dataDump(8:11:end)));
C.num         = str2num(char(dataDump(9:11:end)));
C.mantissa    = str2num(char(dataDump(10:11:end)));
C.name        = char(dataDump(11:11:end));
C.moment      = C.num.*10.^C.mantissa;